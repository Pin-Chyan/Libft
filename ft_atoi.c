int		ft_atoi(const char *str)
{
	int	num;
	int	sign;

	num = 0;
	sign = 1;
	while (*(str) == '\n' ||
			*(str) == '\t' ||
			*(str) == '\r' ||
			*(str) == '\v' ||
			*(str) == '\f' ||
			*(str) == ' ')
		str++;
	if (*(str) == '-')
		sign = -1;
	if (*(str) == '-' || *(str) == '+')
		str++;
	while (*(str) && *(str) >= '0' && *(str) <= '9')
		num = num * 10 + (*(str++) - '0');
	return (num * sign);
}
